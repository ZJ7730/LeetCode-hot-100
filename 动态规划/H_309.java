public class H_309 {


    /**
     * 代码随想录动规五部曲：
     * 1. dp数组含义：dp[i][j] 表示第 i 天结束后处于状态 j 时所得最大现金。
     *    - dp[i][0]：持有股票。
     *    - dp[i][1]：保持卖出股票状态，即不持股且不是今天卖出。
     *    - dp[i][2]：今天卖出股票。
     *    - dp[i][3]：今天是冷冻期。
     * 2. 递推公式见下方状态拆解。
     * 3. 初始化：第 0 天持股为 -prices[0]，其余不持股相关状态为 0。
     * 4. 遍历顺序：从前往后，因为今天状态依赖昨天状态。
     * 5. 解题思路：冷冻期不可持续，且卖出后的下一天不能买入，所以把“不持股”拆成多个互斥状态。
     *
     * 状态一：持有股票状态（今天买入股票，或者是之前就买入了股票然后没有操作，一直持有）
     * 不持有股票状态，这里就有两种卖出股票状态
     * 状态二：保持卖出股票的状态（两天前就卖出了股票，度过一天冷冻期。或者是前一天就是卖出股票状态，一直没操作）
     * 状态三：今天卖出股票
     * 状态四：今天为冷冻期状态，但冷冻期状态不可持续，只有一天！
     * <p>
     * j的状态为：
     * <p>
     * 0：状态一
     * 1：状态二
     * 2：状态三
     * 3：状态四
     * <p>
     * <p>
     * <p>
     * 确定递推公式
     * 达到买入股票状态（状态一）即：dp[i][0]，有两个具体操作：
     * <p>
     * 操作一：前一天就是持有股票状态（状态一），dp[i][0] = dp[i - 1][0]
     * 操作二：今天买入了，有两种情况
     * 前一天是冷冻期（状态四），dp[i - 1][3] - prices[i]
     * 前一天是保持卖出股票的状态（状态二），dp[i - 1][1] - prices[i]
     * 那么dp[i][0] = max(dp[i - 1][0], dp[i - 1][3] - prices[i], dp[i - 1][1] - prices[i]);
     * <p>
     * 达到保持卖出股票状态（状态二）即：dp[i][1]，有两个具体操作：
     * <p>
     * 操作一：前一天就是状态二
     * 操作二：前一天是冷冻期（状态四）
     * dp[i][1] = max(dp[i - 1][1], dp[i - 1][3]);
     * <p>
     * 达到今天就卖出股票状态（状态三），即：dp[i][2] ，只有一个操作：
     * <p>
     * 昨天一定是持有股票状态（状态一），今天卖出
     * <p>
     * 即：dp[i][2] = dp[i - 1][0] + prices[i];
     * <p>
     * 达到冷冻期状态（状态四），即：dp[i][3]，只有一个操作：
     * <p>
     * 昨天卖出了股票（状态三）
     * <p>
     * dp[i][3] = dp[i - 1][2];
     * <p>
     * 综上分析，递推代码如下：
     * <p>
     * dp[i][0] = max(dp[i - 1][0], max(dp[i - 1][3], dp[i - 1][1]) - prices[i]);
     * dp[i][1] = max(dp[i - 1][1], dp[i - 1][3]);
     * dp[i][2] = dp[i - 1][0] + prices[i];
     * dp[i][3] = dp[i - 1][2];
     */


    public int maxProfit(int[] prices) {
        int[][] dp = new int[prices.length][4];

        dp[0][0] = -prices[0];
        dp[0][1] = 0;
        dp[0][2] = 0;
        dp[0][3] = 0;

        for (int i = 1; i < prices.length; i++) {
            dp[i][0] = Math.max(dp[i - 1][0], Math.max(dp[i - 1][3], dp[i - 1][1]) - prices[i]);
            dp[i][1] = Math.max(dp[i - 1][1], dp[i - 1][3]);
            dp[i][2] = dp[i - 1][0] + prices[i];
            dp[i][3] = dp[i - 1][2];
        }

        return Math.max(Math.max(dp[prices.length - 1][1], dp[prices.length - 1][2]), dp[prices.length - 1][3]);


    }


}
