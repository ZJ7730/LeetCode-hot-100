/**
 * @program: suanfa
 * @ClassName: H_64
 * @description: 64. 最小路径和
 *
 * 面试笔记：
 * - 题目定位：给定一个 m * n 的非负整数网格，从左上角走到右下角，每次只能向右或向下移动，
 *   求路径上所有数字之和的最小值。
 * - 核心思路：这是网格路径 DP。因为只能向右或向下走，所以到达任意格子 (i, j) 的上一步只可能来自
 *   上方 (i - 1, j) 或左方 (i, j - 1)，当前位置的最小路径和就从这两个来源里取更小值。
 * - dp 含义：dp[i][j] 表示从左上角 grid[0][0] 走到坐标 (i, j) 时，路径数字和的最小值。
 *   这个定义包含了当前格子 grid[i][j] 本身。
 * - 递推公式：普通位置可以从上方或左方到达，所以
 *   dp[i][j] = Math.min(dp[i - 1][j], dp[i][j - 1]) + grid[i][j]。
 * - 初始化：dp[0][0] = grid[0][0]；第一列只能一直向下走，只能由上一个格子累加；
 *   第一行只能一直向右走，只能由左一个格子累加。
 * - 遍历顺序：从上到下、从左到右遍历，因为 dp[i][j] 依赖的上方和左方状态都必须提前算好。
 * - 返回结果：右下角 dp[grid.length - 1][grid[0].length - 1] 就是从左上到右下的最小路径和。
 * - 注意事项：这里求的是最小路径和，不是路径条数；递推时是取 Math.min 后再加当前格子的值。
 * - 复杂度：时间 O(m * n)，每个格子计算一次；空间 O(m * n)，使用二维 dp 数组保存状态。
 *
 * @author: zhoujie07
 * @create: 2026-05-26
 **/
public class H_64 {
    /**
     * 动规五部曲：
     * 1. dp 数组含义：dp[i][j] 表示从左上角走到坐标 (i, j) 的最小路径和。
     * 2. 递推公式：当前位置只能从上方或左方到达，选择路径和更小的来源：
     *    dp[i][j] = Math.min(dp[i - 1][j], dp[i][j - 1]) + grid[i][j]。
     * 3. 初始化：dp[0][0] = grid[0][0]；第一行只能从左边累加，第一列只能从上边累加。
     * 4. 遍历顺序：从左到右、从上到下，保证上方和左方状态已经计算完成。
     * 5. 解题思路：和不同路径类似，只是“路径数相加”变成“路径和取最小”。
     */

    public int minPathSum(int[][] grid) {
        // 1. dp[i][j] 表示从左上角走到 (i, j) 的最小路径和
        int[][] dp = new int[grid.length][grid[0].length];
        // 2. 起点没有其他来源，最小路径和就是起点自身的值
        dp[0][0] = grid[0][0];

        // 3. 第一列只能从上往下走，所以只能不断累加上方格子的最小路径和
        for (int i = 1; i < grid.length; i++) {
            dp[i][0] = dp[i - 1][0] + grid[i][0];
        }

        // 4. 第一行只能从左往右走，所以只能不断累加左边格子的最小路径和
        for (int i = 1; i < grid[0].length; i++) {
            dp[0][i] = dp[0][i - 1] + grid[0][i];
        }

        // 5. 普通格子可以从上方或左方到达，选择路径和更小的方向
        for (int i = 1; i < grid.length; i++) {
            for (int j = 1; j < grid[0].length; j++) {
                dp[i][j] = Math.min(dp[i - 1][j], dp[i][j - 1]) + grid[i][j];
            }
        }

        // 6. 右下角保存的就是从左上角到右下角的最小路径和
        return dp[grid.length - 1][grid[0].length - 1];
    }


}
